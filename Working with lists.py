# WORKING WITH LISTS

pizzas = ['bbq ', 'ham', 'cheese & garlic']
for pizza in pizzas:
    print(f"I like {pizza} pizza very much")
print("\nI love pizza from dominos because they have a wider range of options.\n\n")

# 4-2 ANIMALS

animals = ['dog','cat','fish']

for animal in animals:
    print("Type of the animal is ", animal)
    print(f'{animal.upper()} would make a great pet.')
print(f"\nIt is  viable to have any of these animals as your pet.")

